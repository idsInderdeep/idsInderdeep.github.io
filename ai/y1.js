let video;
let poseNet;
let pose;
let skeleton;

let brain;
let poseLabel = "LOADING MODEL";

function setup() {
  let constraints = {
    video: {
      mandatory: {
        minWidth: 1200,
        minHeight: 700
      },
      optional: [{ maxFrameRate: 10 }]
    },
    audio: false
  };	
	
	
  createCanvas(1200, 700);
  
  
  
  video = createCapture(constraints);
  video.hide();
  poseNet = ml5.poseNet(video, modelLoaded);
  poseNet.on('pose', gotPoses);

  let options = {
    inputs: 34,
    outputs: 4,
    task: 'classification',
    debug: true
  }
  brain = ml5.neuralNetwork(options);
  const modelInfo = {
    model: '/static/model1/model.json',
    metadata: '/static/model1/model_meta.json',
    weights: '/static/model1/model.weights.bin',
  };
  brain.load(modelInfo, brainLoaded);
}

function brainLoaded() {
  console.log('pose classification ready!');
  classifyPose();
}

function classifyPose() {
  if (pose) {
    let inputs = [];
    for (let i = 0; i < pose.keypoints.length; i++) {
      let x = pose.keypoints[i].position.x/1200;
      let y = pose.keypoints[i].position.y/700;
      inputs.push(x);
      inputs.push(y);
    }
    brain.classify(inputs, gotResult);
  } else {
    setTimeout(classifyPose, 100);
  }
}

function gotResult(error, results) {
  
  if (results[0].confidence > 0.92) {
    poseLabel = results[0].label.toUpperCase();
  }else{poseLabel="_";}
  //console.log(results[0].confidence);
  classifyPose();
}


function gotPoses(poses) {
  if (poses.length > 0) {
    pose = poses[0].pose;
    skeleton = poses[0].skeleton;
  }
}


function modelLoaded() {
  console.log('poseNet ready');
}

function draw() {
  push();
  translate(video.width, 0);
  scale(-1, 1);
  image(video, 0, 0, video.width, video.height);

  if (pose) {
    for (let i = 0; i < skeleton.length; i++) {
      let a = skeleton[i][0];
      let b = skeleton[i][1];
      strokeWeight(2);
      stroke(0);

      line(a.position.x, a.position.y, b.position.x, b.position.y);
    }
    for (let i = 0; i < pose.keypoints.length; i++) {
      let x = pose.keypoints[i].position.x;
      let y = pose.keypoints[i].position.y;
      fill(0);
      stroke(255);
      ellipse(x, y, 16, 16);
    }
  }
  pop();

  fill(255, 0, 255);
  noStroke();
  textSize(80);
  textAlign(CENTER, CENTER);
  
  if(poseLabel=='L'){poseLabel="LEFT LEG TREE";}


  text(poseLabel, width / 2, height / 2);

}
